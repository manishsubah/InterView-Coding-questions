/**
 * README: Time Complexity Java Examples
 * 
 * This directory contains practical Java examples demonstrating different time complexities.
 * Each file focuses on a specific complexity class with multiple examples and explanations.
 * 
 * FILES:
 * ======
 * 
 * 1. ConstantTime.java - O(1) examples
 *    - Array access, HashMap operations, Stack/Queue operations
 *    - Run: javac ConstantTime.java && java ConstantTime
 * 
 * 2. LogarithmicTime.java - O(log n) examples
 *    - Binary search, BST operations, binary exponentiation
 *    - Run: javac LogarithmicTime.java && java LogarithmicTime
 * 
 * 3. LinearTime.java - O(n) examples
 *    - Linear search, finding max/min, array traversal
 *    - Run: javac LinearTime.java && java LinearTime
 * 
 * 4. LinearithmicTime.java - O(n log n) examples
 *    - Merge Sort, Quick Sort, Heap Sort
 *    - Run: javac LinearithmicTime.java && java LinearithmicTime
 * 
 * 5. QuadraticTime.java - O(n²) examples
 *    - Bubble Sort, Selection Sort, nested loops
 *    - Run: javac QuadraticTime.java && java QuadraticTime
 * 
 * 6. ExponentialTime.java - O(2ⁿ) examples
 *    - Naive Fibonacci, subset generation, Tower of Hanoi
 *    - Run: javac ExponentialTime.java && java ExponentialTime
 *    - WARNING: Very slow for large inputs!
 * 
 * 7. FactorialTime.java - O(n!) examples
 *    - Permutations, TSP brute force, N-Queens
 *    - Run: javac FactorialTime.java && java FactorialTime
 *    - WARNING: Extremely slow! Only for very small inputs!
 * 
 * 8. TimeComplexityDemo.java - Performance comparison
 *    - Compares all complexities with timing
 *    - Run: javac TimeComplexityDemo.java && java TimeComplexityDemo
 * 
 * HOW TO USE:
 * ===========
 * 
 * 1. Compile a file:
 *    javac ConstantTime.java
 * 
 * 2. Run the compiled class:
 *    java ConstantTime
 * 
 * 3. Or compile and run in one command:
 *    javac ConstantTime.java && java ConstantTime
 * 
 * LEARNING PATH:
 * ==============
 * 
 * Start with:
 * 1. ConstantTime.java - Understand O(1)
 * 2. LinearTime.java - Understand O(n)
 * 3. LogarithmicTime.java - Understand O(log n)
 * 
 * Then explore:
 * 4. LinearithmicTime.java - See efficient sorting
 * 5. QuadraticTime.java - See why nested loops are slow
 * 
 * Advanced:
 * 6. ExponentialTime.java - See exponential growth
 * 7. FactorialTime.java - See factorial growth
 * 8. TimeComplexityDemo.java - Compare all complexities
 * 
 * TIPS:
 * =====
 * 
 * - Read the comments in each file for detailed explanations
 * - Try modifying the examples to see how complexity changes
 * - Compare the performance of different approaches
 * - Use TimeComplexityDemo.java to see actual timing differences
 * 
 * NOTE:
 * =====
 * - Some examples use helper classes (ListNode, TreeNode) that are defined inline
 * - All examples are self-contained and can be run independently
 * - Performance may vary based on your system
 */

