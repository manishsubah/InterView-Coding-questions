public class StringIntro {

    public static void main(String[] args) {
        String str1 = "hello";
        String str2 = "java";

        System.out.println("str1: " + str1.length());
        System.out.println("str2: " + str2.length());

        System.out.println("str1 + str2: " + (str1 + str2).length());
        
    }
}
